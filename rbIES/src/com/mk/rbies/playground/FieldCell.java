package com.mk.rbies.playground;

public class FieldCell {
	private int fieldCoordX;
	private int fieldCoordY;
	private int fieldValue;

	public int getFieldCoordX() {
		return fieldCoordX;
	}

	public void setFieldCoordX(int fieldCoordX) {
		this.fieldCoordX = fieldCoordX;
	}

	public int getFieldCoordY() {
		return fieldCoordY;
	}

	public void setFieldCoordY(int fieldCoordY) {
		this.fieldCoordY = fieldCoordY;
	}

	public int getFieldValue() {
		return fieldValue;
	}

	public void setFieldValue(int fieldValue) {
		this.fieldValue = fieldValue;
	}

}
